
public class Switch {
	
	public static void main(String[] args) {
		
		String day = "Mondays";
		
		switch(day) {
			case "Sunday": System.out.println("It is Sunday");
			break;
			case "Monday": System.out.println("It's Monday");
			break;
			default: System.out.println("Not Sunday or Monday");
		}
	}
	
}
